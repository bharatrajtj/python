def add(a,b):       ###Function takes Input a,b
    add = a + b     ###Performs logic
    return add      ### Returns output to the function

def sub(a,b):
    sub = a - b
    return sub

def mul(a,b):
    mul = a * b
    return mul


print(add(7,5))   ### Return keyword replaces value for add(7,5)
print(sub(100,48))
print(mul(3,97))
